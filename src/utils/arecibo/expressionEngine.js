/**
 * @fileoverview Arecibo Expression Engine
 * Main integration point: assembles week context, calls LLM if budget allows,
 * generates AreciboIntent with fallback to templates
 *
 * Core contract:
 * - Input: weekData (from factStore), bondLevel, tasteProfile, llmBudget
 * - Output: AreciboIntent matching the spec
 * - Fallback: deterministic template when LLM unavailable
 * - Timeout: 8 seconds (don't block UI)
 */

